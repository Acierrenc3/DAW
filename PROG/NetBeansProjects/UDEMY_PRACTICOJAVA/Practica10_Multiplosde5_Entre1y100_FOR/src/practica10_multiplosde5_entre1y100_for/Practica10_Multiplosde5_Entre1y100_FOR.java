
package practica10_multiplosde5_entre1y100_for;

//Escribir un programa que muestre por pantalla todos los multiplos de 5 entre 1 y 100

public class Practica10_Multiplosde5_Entre1y100_FOR {
    public static void main(String[] args) {
        
        for (int i = 5; i <= 100;  i=i+5) {
            System.out.println("El multiplo de 5 es: " + i);
            
        }
        

    }
    
}
